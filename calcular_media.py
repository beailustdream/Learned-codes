# 1. Receber a primeira nota do aluno
nota1 = float(input("Digite a primeira nota: "))

# 2. Receber a segunda nota do aluno
nota2 = float(input("Digite a segunda nota: "))

# 3. Receber a terceira nota do aluno
nota3 = float(input("Digite a terceira nota: "))

# 4. Somar as três notas
soma = nota1 + nota2 + nota3

# 5. Dividir a soma por 3 para obter a média
media = soma / 3

# 6. Exibir o resultado da média na tela
print(f"A média do aluno é: {media}")